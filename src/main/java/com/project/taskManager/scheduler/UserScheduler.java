package com.project.taskManager.scheduler;

import com.project.taskManager.cache.AppCache;
import com.project.taskManager.entity.TaskEntry;
import com.project.taskManager.entity.User;
import com.project.taskManager.enums.Sentiment;
import com.project.taskManager.repository.UserRepositoryImpl;
import com.project.taskManager.service.EmailService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.stereotype.Component;

import java.time.LocalDateTime;
import java.time.temporal.ChronoUnit;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;

@Component
public class UserScheduler {
    @Autowired
    private EmailService emailService;

    @Autowired
    private UserRepositoryImpl userRepository;

    @Autowired
    private AppCache appCache;

    @Scheduled(cron = "0 0 9 * * MON")
    public void fetchUsersAndSendSaMail(){
        List<User> users = userRepository.getUserForSA();
        for (User user : users) {
            List<TaskEntry> journalEntries = user.getTaskEntries();
            List<Sentiment> sentiments = journalEntries.stream().filter(x -> x.getDate().isAfter(LocalDateTime.now().minus(7, ChronoUnit.DAYS))).map(x -> x.getSentiment()).collect(Collectors.toList());
            Map<Sentiment, Integer> sentimentCounts = new HashMap<>();
            for (Sentiment sentiment : sentiments) {
                if (sentiment != null)
                    sentimentCounts.put(sentiment, sentimentCounts.getOrDefault(sentiment, 0) + 1);
            }
            Sentiment mostFrequentSentiment = null;
            int maxCount = 0;
            for (Map.Entry<Sentiment, Integer> entry : sentimentCounts.entrySet()) {
                if (entry.getValue() > maxCount) {
                    maxCount = entry.getValue();
                    mostFrequentSentiment = entry.getKey();
                }
            }
            if (mostFrequentSentiment != null) {
               emailService.sendEmail(user.getEmail(), "Sentimemt for last 7 days",mostFrequentSentiment.toString());
            }
        }
    }

//
}
